<script>
	//导入md5加密包
	import w_md5 from "../../../js_sdk/zww-md5/w_md5.js"
	export default {
		data() {
			return {
				userName:'',					//用户名
				userRootKey:'',					//用户权限密钥
				userAge:'',						//用户年龄
				userEmail:'',					//用户邮箱
				userGender:'',					//用户性别
				userPassword:'',				//用户密码
				userPpassword:'',				//用户第二次去人密码
				genderList:["男","女"],			//用户性别列表
				index:-1,						//下拉索引
				
				
				//2
				registerPhone: '',
				registerPassword: '',
				confirmPassword: '',
				registerCode: '',
				smsbtn: {
					text: '发送',
					status: false,
					codeTime: 60
				},
				timerId: null,
				message: ''
			}
		},
		
		computed: {								//按钮风格
			buttonStyle() {
				let style = {};
				style.color = "#fff";
				style.backgroundColor = this.$u.color['warning'];
				return style;
			},
		},
		
		components: {
			
		},
		methods: {
			onLoad(){
				
			},
			
			clickGetCode(){
				this.$u.toast("点击了获取验证码");
			},
			
			//点击取消按钮
			cancelButton(){
				this.$u.toast("取消注册并返回登陆界面")
				uni.redirectTo({
					url:"../login/login",
				})
			},
			
			//注册按钮
			registButton(){
				//前期判断信息是否完善和密码是否一致
				if(this.userName==""||this.userPassword==""||this.userPpassword==""){
					this.$u.toast("请完善信息后再点击注册")
					return
				}
				if(this.userPassword!=this.userPpassword){
					this.$u.toast("您的密码前后不一致")
					return
				}
				
				//检验密码位数是否符合要求
				if(this.userPassword.length!=6){
					this.$u.toast("您的密码格式不正确，请输入6位密码");
					return;
				}
				
				//this指针
				var _this=this
				
				//暂时直接提示后杀死
				this.$u.toast("点击了注册");
				return;
				
				uni.showModal({
					title: '确认注册',
					content: '确认注册信息无误并提交？',
					success: function (res) {
						if (res.confirm) {
							uni.request({
								url: '/api/createUser', 
								method:'POST',
								//传送用户名、管理员令牌、密码，性别、年龄邮箱
								// data:{userName:_this.userName,
								// 		managerKey:_this.userRootKey,
								// 		password:_this.userPassword,
								// 		regAge:_this.userAge,
								// 		regSex:_this.userGender,
								// 		regEmail:_this.userEmail}, 
								success: (res) => {
									console.log(res.data);
									return;					//先直接杀掉，后续再处理
									if(!res.data.data.status){
										_this.$u.toast("用户名已存在，请更改")
										return;
									}
									uni.navigateBack({
										delta:1,
									})
								}
							});
						} else if (res.cancel) {
							console.log('取消注册');
						}
					}
				});
			},
			
			//2
			getsmscode() {
				//此处写发送验证码逻辑
				if (this.smsbtn.codeTime != 60) {
					return;
				}
				this.timerId = setInterval(() => {//此处直接复制了另一个插件里的计时器，在插件市场里搜索登录，时间最靠前的那位
						let codeTime = this.smsbtn.codeTime;
						codeTime--;
						this.smsbtn.codeTime = codeTime;
						this.smsbtn.text = codeTime + "S";
						if (codeTime < 1) {
							clearInterval(this.timerId);
							this.smsbtn.text = "重试";
							this.smsbtn.codeTime = 60;
							this.smsbtn.status = false;
						}
					},
					1000);
				return false;
			},
			goRegister() {
				let registerPhone = this.registerPhone;
				let registerPassword = this.registerPassword;
				let confirmPassword = this.confirmPassword;
				let registerCode = this.registerCode;
				if (!(/^1(3|4|5|6|7|8|9)\d{9}$/.test(registerPhone))) {
					this.message = "手机号码有误，请重填";
					return false;
				}
				if (registerCode < 100000) {
					this.message = "验证码不符合格式";
					return false;
				}
				if (!registerPassword) {
					this.message = "密码为空";
					return false;
				}
			
				let ls = 0;
				if (registerPassword.match(/([a-z])+/)) {
					ls++;
				}
				if (registerPassword.match(/([0-9])+/)) {
					ls++;
				}
				if (registerPassword.match(/([A-Z])+/)) {
					ls++;
				}
				if (registerPassword.match(/[^a-zA-Z0-9]+/)) {
					ls++;
				}
				if (registerPassword.length < 8) {
					ls = 0;
				}
				if (ls < 2) {
					this.message = "密码强度不够，至少8位，大写、小写、字母、符号 其中两种";
					return false;
				}
			
			
				if (confirmPassword != registerPassword) {
					this.message = "两次密码不同";
					return false;
				}
				uni.showLoading({
					title: '加载中。。。',
					mask: false
				});
			
				let headers = {};
				headers['content-type'] = 'application/x-www-form-urlencoded; charset=UTF-8';
				let PHPSESSID = uni.getStorageSync('PHPSESSID');
				if (PHPSESSID) {
					headers['cookie'] = 'PHPSESSID=' + PHPSESSID;//将PHPSESSID放入请求头中,如你有其他cookies都可以缀后面，分号分割。浏览器端本身就有cookies机制，不设置
				}
				uni.request({
					url: this.$url + '/api/login/register.php',//此处使用了全局变量拼接url（main.js文件中），关于全局变量官方问答里有
					method: 'POST',
					header: headers,
					data: {
						phone: this.registerPhone, //phone应该以后台验证码接收到的为phone，否则会造成修改后任意手机号注册漏洞，本demo不作处理
						pw: this.registerPassword //本demo没有传输验证码，自行传输
					},
					success: res => {
						console.log(res);
						let cookies = res.cookies;
						if (cookies) {
							for (let i = 0; i < cookies.length; i++) {
								if (cookies[i].name == 'PHPSESSID') {//PHPSESSID从cookies取出，放入本地储存
									uni.setStorageSync('PHPSESSID', cookies[i].value);
									break;
								}
							}
						}
						//返回的基本信息做本地缓存
						let data = res.data;
						if (data.ec === 0) {
							uni.setStorageSync('userinfo', data.user);
							uni.hideLoading();
							uni.reLaunch({
								url: '../index/indexme'
							});
						} else {
							uni.removeStorageSync('userinfo');
							this.message = data.msg;
							uni.hideLoading();
						}
					},
					fail: () => {
						uni.hideLoading();
						this.message = "网络连接失败";
					},
					complete: () => {}
				});
			},
			openAgreement() {
				uni.navigateTo({
					url: '../login/userAgreement',
					success: res => {},
					fail: () => {},
					complete: () => {}
				});
			}
		},
	};
</script>

